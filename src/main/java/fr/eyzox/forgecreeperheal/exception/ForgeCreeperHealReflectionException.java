package fr.eyzox.forgecreeperheal.exception;

public class ForgeCreeperHealReflectionException extends ForgeCreeperHealException {

	public ForgeCreeperHealReflectionException() {
		super();
	}

	public ForgeCreeperHealReflectionException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ForgeCreeperHealReflectionException(String message, Throwable cause) {
		super(message, cause);
	}

	public ForgeCreeperHealReflectionException(String message) {
		super(message);
	}

	public ForgeCreeperHealReflectionException(Throwable cause) {
		super(cause);
	}

}
