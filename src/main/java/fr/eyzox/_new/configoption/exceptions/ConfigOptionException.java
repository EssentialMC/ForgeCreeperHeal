package fr.eyzox._new.configoption.exceptions;

public class ConfigOptionException extends RuntimeException {

	public ConfigOptionException() {
	}

	public ConfigOptionException(String arg0) {
		super(arg0);
	}

	public ConfigOptionException(Throwable arg0) {
		super(arg0);
	}

	public ConfigOptionException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ConfigOptionException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
