package fr.eyzox.forgecreeperheal.exception;

public class ForgeCreeperHealerSerialException extends ForgeCreeperHealException {

	public ForgeCreeperHealerSerialException() {
		super();
	}

	public ForgeCreeperHealerSerialException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ForgeCreeperHealerSerialException(String message, Throwable cause) {
		super(message, cause);
	}

	public ForgeCreeperHealerSerialException(String message) {
		super(message);
	}

	public ForgeCreeperHealerSerialException(Throwable cause) {
		super(cause);
	}

}
